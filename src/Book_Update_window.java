    private void jLabel12_Book_UpdateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel12_Book_UpdateMousePressed
        // TODO add your handling code here:
        try{
             String update_query = "UPDATE `books` SET `ISBN`=?,`Name`=?,`Author`=?,`Edition`=? WHERE `Accession_Number`=?";
            conn = DriverManager.getConnection("jdbc:mysql://localhost/libot", "root", "tinotenda");
            pst = conn.prepareStatement(update_query);
            pst.setString(5, jTextField_Update_book_search_by_acc_number.getText());
            pst.setString(1, txt_Book_Update_ISBN.getText());
            pst.setString(2, txt_Book_Update_Name.getText());
            pst.setString(3, txt_Book_Update_Author.getText());
            pst.setString(4, txt_Book_Update_Edition.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update Successfull");
            jTextField_Update_book_search_by_acc_number.setText("");
            txt_Book_Update_ISBN.setText("");
            txt_Book_Update_Name.setText("");
            txt_Book_Update_Author.setText("");
            txt_Book_Update_Edition.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Update failed");
        }
    }//GEN-LAST:event_jLabel12_Book_UpdateMousePressed

    private void jLabel9_Update_search_button1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9_Update_search_button1MousePressed
        // TODO add your handling code here:
       try{
            String query = "SELECT * FROM `books` WHERE `Accession_Number` = ?";
            conn = DriverManager.getConnection("jdbc:mysql://localhost/libot", "root", "tinotenda");
            pst = conn.prepareStatement(query);
            pst.setString(1, jTextField_Update_book_search_by_acc_number.getText());
            rs= pst.executeQuery();
            
            if(rs.next()){
                txt_Book_Update_ISBN.setEditable(true);
                txt_Book_Update_Name.setEditable(true);
                txt_Book_Update_Author.setEditable(true);
                txt_Book_Update_Edition.setEditable(true);
                //jTextField_Update_book_search_by_acc_number.setText("");
               jTextField_Update_book_search_by_acc_number.setEditable(false);
               jLabel9_Update_Enable.setVisible(true);
               
                
                String setISBN = rs.getString("ISBN");
                txt_Book_Update_ISBN.setText(setISBN);

                String setName = rs.getString("Name");
                txt_Book_Update_Name.setText(setName);

                String setAuthor = rs.getString("Author");
                txt_Book_Update_Author.setText(setAuthor);

                String setEdition = rs.getString("Edition");
                txt_Book_Update_Edition.setText(setEdition);

                
               // txt_Date_Logged_In_Update.setText(setDate);
                
            }else{
                JOptionPane.showMessageDialog(this, "Book not found, check your syntax!");
                txt_Book_Update_ISBN.setText("");
                txt_Book_Update_Name.setText("");
                txt_Book_Update_Author.setText("");
                txt_Book_Update_Edition.setText("");
                jTextField_Update_book_search_by_acc_number.setText("");
               // txt_Date_Logged_In_Update.setText("");
                txt_Book_Update_ISBN.setEditable(false);
                txt_Book_Update_Name.setEditable(false);
                txt_Book_Update_Author.setEditable(false);
                txt_Book_Update_Edition.setEditable(false);
                //jDateChooser1_Update.setEnabled(false);
            }
        }catch(Exception ex){
        }
    }//GEN-LAST:event_jLabel9_Update_search_button1MousePressed

