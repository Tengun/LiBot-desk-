    private void jLabel3_update_codeMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3_update_codeMousePressed
        // TODO add your handling code here:

        try {
            String code_update = "UPDATE `super_code` SET `New_Code` = ? WHERE `Current_Code` = ?";
            conn = DriverManager.getConnection("jdbc:mysql://localhost/libot", "root", "tinotenda");
            pst = conn.prepareStatement(code_update);
            pst.setString(2, txt_Current_Code.getText());
            pst.setString(1, txt_New_Code.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Code update successfull");
            txt_Code_Shield_Number.setText("");
            txt_Current_Code.setText("");
            txt_New_Code.setText("");
           new Settings_window().setVisible(true);
           dispose();
        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error occured while updating code");
        }
    }//GEN-LAST:event_jLabel3_update_codeMousePressed

