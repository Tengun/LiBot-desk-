    private void jLabel_code_checkMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_code_checkMousePressed
        // TODO add your handling code here:
         try {
            String query = "SELECT `Current_Code` FROM `super_code` WHERE `Code_Shield_Number` = ?";
            conn = DriverManager.getConnection("jdbc:mysql://localhost/libot", "root", "tinotenda");
            pst = conn.prepareStatement(query);
            pst.setString(1, txt_Code_Shield_Number.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                String setCurrent_Code = rs.getString("Current_Code");
                txt_Current_Code.setText(setCurrent_Code);
                jLabel_code_check.setVisible(false);
            } else {
            JOptionPane.showMessageDialog(this, "Code Shield not found?");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Null");
        }
    }//GEN-LAST:event_jLabel_code_checkMousePressed

