    private void jLabel9_Student_search_buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9_Student_search_buttonMousePressed
        // TODO add your handling code here:
        try {
            String query = "SELECT * FROM `students` WHERE `Reg No` = ?";
            conn = DriverManager.getConnection("jdbc:mysql://localhost/libot", "root", "tinotenda");
            pst = conn.prepareStatement(query);
            pst.setString(1, jTextField_Update_student_search_by_Reg_Number.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                txt_Student_Update_Name.setEditable(true);
                txt_Student_Update_Surname.setEditable(true);
                txt_Student_Update_ID.setEditable(true);
                txt_Update_Student_Gender.setEditable(true);
                txt_Update_Student_Course.setEditable(true);
                txt_Update_Student_Level.setEditable(true);
                txt_Update_Student_Intake.setEditable(true);
                txt_Update_Student_Intake_Year.setEditable(true);
                jTextField_Update_student_search_by_Reg_Number.setEditable(false);
                jLabel9_Student_Update_Enable.setVisible(true);
                /*---------------------------------------------------*/
                String setStudent_name = rs.getString("Name");
                txt_Student_Update_Name.setText(setStudent_name);

                String setStudent_surname = rs.getString("Surname");
                txt_Student_Update_Surname.setText(setStudent_surname);

                String setStudent_ID = rs.getString("National_ID");
                txt_Student_Update_ID.setText(setStudent_ID);

                String setStudent_gender = rs.getString("Gender");
                txt_Update_Student_Gender.setText(setStudent_gender);

                String setCourse = rs.getString("Course");
                txt_Update_Student_Course.setText(setCourse);

                String setStudent_level = rs.getString("Level");
                txt_Update_Student_Level.setText(setStudent_level);

                String setIntake = rs.getString("Intake");
                txt_Update_Student_Intake.setText(setIntake);

                String setIntake_Year = rs.getString("Intake_Year");
                txt_Update_Student_Intake_Year.setText(setIntake_Year);
            } else {
                JOptionPane.showMessageDialog(this, "Book not found, check your syntax!");
                txt_Student_Update_Name.setText("");
                txt_Student_Update_Surname.setText("");
                txt_Student_Update_ID.setText("");
                txt_Update_Student_Gender.setText("");
                txt_Update_Student_Course.setText("");
                txt_Update_Student_Level.setText("");
                txt_Update_Student_Intake.setText("");
                txt_Update_Student_Intake_Year.setText("");
                jTextField_Update_student_search_by_Reg_Number.setText("");
                /*-------------------------------------------*/
                txt_Student_Update_Name.setEditable(false);
                txt_Student_Update_Surname.setEditable(false);
                txt_Student_Update_ID.setEditable(false);
                txt_Update_Student_Gender.setEditable(false);
                txt_Update_Student_Course.setEditable(false);
                txt_Update_Student_Level.setEditable(false);
                txt_Update_Student_Intake.setEditable(false);
                txt_Update_Student_Intake_Year.setEditable(false);
            }
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_jLabel9_Student_search_buttonMousePressed

