                pst.setString(9, jLabelStatus.getText());
                pst.setString(10, (String) jCombo_Intake_Year.getSelectedItem());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Student Log Successfull");
                txt_Student_Name.setText("");
                txt_Student_Surname.setText("");
                txt_Student_ID_Number.setText("");
